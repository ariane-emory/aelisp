#include "core_includes.h"
#include "eval.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
// _eval
////////////////////////////////////////////////////////////////////////////////////////////////////

DEF_CORE_FUN(eval) {
  RETURN(EVAL(env, CAR(args)));
  
  END_DEF_CORE_FUN(eval);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// _list
////////////////////////////////////////////////////////////////////////////////////////////////////

DEF_CORE_FUN(list) {
  RETURN(args);
  
  END_DEF_CORE_FUN(list);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// _quote
////////////////////////////////////////////////////////////////////////////////////////////////////

DEF_CORE_FUN(quote) {
  RETURN(CAR(args));

  END_DEF_CORE_FUN(quote);
}

