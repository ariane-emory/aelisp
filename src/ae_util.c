#include "ae_util.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
// Data
////////////////////////////////////////////////////////////////////////////////////////////////////

char obj_log_buffer[64] = {0};
int  indentation        = 0;

////////////////////////////////////////////////////////////////////////////////////////////////////
// obj_log
////////////////////////////////////////////////////////////////////////////////////////////////////

void obj_log(const ae_obj_t * const obj, char * desc) {
  NL;

  int written = 0;

  while (written++ < (indentation << 1)) SPC;
  
  written += PR("%s ", desc);

  while (written++ < 72) SPC;
  
  written += WRITE(obj);

  fflush(stdout);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// indent
////////////////////////////////////////////////////////////////////////////////////////////////////

void indent(void) {
  indentation += 1;
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// outdent
////////////////////////////////////////////////////////////////////////////////////////////////////

void outdent(void) {
  indentation -= 1;

  if (indentation <= 0)
    indentation = 0;
}
