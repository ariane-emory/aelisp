#include <time.h>

#include "time_funcs.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
// now
////////////////////////////////////////////////////////////////////////////////////////////////////
long long int now(void) {
    struct timespec ts;
    clock_gettime(CLOCK_MONOTONIC, &ts);
    return (long long) ts.tv_sec * 1000LL + ts.tv_nsec / 1000000LL; // Convert seconds to milliseconds and add nanoseconds converted to milliseconds
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// now_us
////////////////////////////////////////////////////////////////////////////////////////////////////

long long int now_us(void) {
    struct timespec ts;
    clock_gettime(CLOCK_MONOTONIC, &ts);
    return (long long) ts.tv_sec * 1000000LL + ts.tv_nsec / 1000LL; // Convert seconds to microseconds and add nanoseconds converted to microseconds
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// elapsed
////////////////////////////////////////////////////////////////////////////////////////////////////
long long int elapsed(long long int time) {
  return ((long long int) (now() - time));
}

////////////////////////////////////////////////////////////////////////////////////////////////////
// elapsed_us
////////////////////////////////////////////////////////////////////////////////////////////////////
long long int elapsed_us(long long int time_us) {
  return ((long long int) (now_us() - time_us));
}

