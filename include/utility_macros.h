#define defer(var_decl, cleanup_stmts)                                                             \
  var_decl;                                                                                        \
  for (int CONCAT(_defer_flag_, __LINE__) = 1;                                                     \
       CONCAT(_defer_flag_, __LINE__);                                                             \
       ({                                                                                          \
         (CONCAT(_defer_flag_, __LINE__) = 0);                                                     \
         cleanup_stmts;                                                                            \
         (void)0;                                                                                  \
       }))
